function u32FromRgb(r: number, g: number, b: number): number {
  return ((255 << 24) | (b << 16) | (g << 8) | r) >>> 0
}

const CYAN_HEX =
  `#${CYAN.r.toString(16).padStart(2, '0')}${CYAN.g.toString(16).padStart(2, '0')}${CYAN.b.toString(16).padStart(2, '0')}` as const
